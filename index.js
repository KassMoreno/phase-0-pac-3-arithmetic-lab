function add(x,y){
    return (x+y)}
function subtract(x,y){
    return (x-y)}
function multiply(x,y){
    return (x*y)}
function divide(x,y){
    return(x/y)}
function increment(number){
    if (number+"++") ("++"+number)
    return(number+1)}
function decrement(number){
    if (number+"--") ("--"+number)
    return(number-1)}
function makeInt(x){
    return parseInt(x,10)}
function preserveDecimal(x){
    return parseFloat(x,10)}